Canvas::Canvas()
	: myHeight(0)
	, myWidth(0)
	, myTexture(nullptr)
	, myBlendMode(BlendMode::Add)
{
}

Canvas::~Canvas()
{
	delete[] myTexture;
}

void Canvas::Setup(size_t aWidth, size_t aHeight, V4F aClearColor)
{
	if (myTexture)
		delete[] myTexture;

	myWidth = aWidth;
	myHeight = aHeight;

	myTexture = new V4F[myWidth * myHeight];
	for (size_t i = 0; i < myWidth * myHeight; i++)
		myTexture[i] = V4F(aClearColor, 1);
}

ID3D11ShaderResourceView* Canvas::Export(ID3D11Device* aDevice)
{
	ID3D11ShaderResourceView* view;

	{
		D3D11_TEXTURE2D_DESC desc;
		WIPE(desc);
		desc.Width = myWidth;
		desc.Height = myHeight;
		desc.MipLevels = 1;
		desc.ArraySize = 1;
		desc.Format = DXGI_FORMAT_R32G32B32A32_FLOAT;
		desc.SampleDesc.Count = 1;
		desc.SampleDesc.Quality = 0;
		desc.Usage = D3D11_USAGE_DEFAULT;
		desc.BindFlags = D3D11_BIND_SHADER_RESOURCE;
		desc.CPUAccessFlags = 0;
		desc.MiscFlags = 0;

		D3D11_SUBRESOURCE_DATA data;
		WIPE(data);
		data.pSysMem = myTexture;
		data.SysMemPitch = myWidth * sizeof(V4F);
		data.SysMemSlicePitch = myWidth * myHeight * sizeof(V4F);


		ID3D11Texture2D* tex;
		HRESULT res = aDevice->CreateTexture2D(&desc, &data, &tex);

		if (FAILED(res))
		{
			LOGERROR("Failed to create texture");
			return nullptr;
		}

		D3D11_SHADER_RESOURCE_VIEW_DESC viewDesc;
		WIPE(viewDesc);

		viewDesc.Format = DXGI_FORMAT_R32G32B32A32_FLOAT;
		viewDesc.Texture2D.MipLevels = 1;
		viewDesc.Texture2D.MostDetailedMip = 0;
		viewDesc.ViewDimension = D3D_SRV_DIMENSION_TEXTURE2D;
		res = aDevice->CreateShaderResourceView(tex, &viewDesc, &view);

		tex->Release();
		if (FAILED(res))
		{
			LOGERROR("Failed to create view");
			return nullptr;
		}
	}

	return view;
}

size_t Canvas::GetWidth()
{
	return myWidth;
}

size_t Canvas::GetHeight()
{
	return myHeight;
}

void Canvas::DrawPixel(Point aPoint, V4F aColor)
{
	if (aPoint.x < 0 || aPoint.y < 0 || aPoint.x >= myWidth || aPoint.y >= myHeight)
	{
		return;
	}
	size_t index = aPoint.y * myWidth + aPoint.x;
	switch (myBlendMode)
	{
	case BlendMode::Add:
		myTexture[index] = aColor + LERP(myTexture[index], V4F(0, 0, 0, 0), aColor.w);
		myTexture[index].x = CLAMP(0, 1, myTexture[index].x);
		myTexture[index].y = CLAMP(0, 1, myTexture[index].y);
		myTexture[index].z = CLAMP(0, 1, myTexture[index].z);
		break;
	case BlendMode::Replace:
		myTexture[index] = aColor;
		break;
	default:
		break;
	}
}

void Canvas::DrawBox(Point aMin, Point aMax, V4F aColor, bool aFilled, Pattern aPattern)
{
	if (aFilled)
	{
		for (int y = aMin.y; y <= aMax.y; y++)
		{
			for (int x = aMin.x; x <= aMax.x; x++)
			{
				DrawPixel({x, y}, aColor);
			}
		}
	}
	else
	{
		for (int y = aMin.y + 1; y <= aMax.y - 1; y++)
		{
			if (aPattern[(y - aMin.y) % aPattern.size()])
			{
				DrawPixel({aMin.x, y}, aColor);
				DrawPixel({aMax.x, y}, aColor);
			}
		}
		for (int x = aMin.x; x <= aMax.x; x++)
		{
			if (aPattern[(x - aMin.x) % aPattern.size()])
			{
				DrawPixel({x, aMin.y}, aColor);
				DrawPixel({x, aMax.y}, aColor);	
			}
		}
	}
}

void
Canvas::DrawText(const std::string& aText, Point aBottomLeft, V4F aColor)
{
	Point at = aBottomLeft;

	const static CommonUtilities::Vector2<size_t> glyphSize { 8, 13 };

	for (char c : aText)
	{
		for (int y = at.y; y < at.y + glyphSize.y; y++)
		{
			for (int x = at.x; x < at.x + 8; x++)
			{
				auto& glyph = glyphs[c-32];
				unsigned char bitMask = glyph[glyphSize.y + at.y - y - 1];
				if ((bitMask & BIT(glyphSize.x + at.x - x - 1)) != 0)
				{
					DrawPixel({x, y}, aColor);
				}
			}
		}
		at.x += glyphSize.x + 1;
	}
}

void
Canvas::DrawLine(Point aStart, Point aEnd, V4F aColor, Pattern aPattern )
{

	if (abs(aEnd.x - aStart.x) > abs(aEnd.y - aStart.y))
	{
		Point	start{MIN(aStart.x, aEnd.x), aStart.y};
		Point	end{MAX(aStart.x, aEnd.x), aEnd.y};
		for (int x = start.x; x <= end.x; x++)
		{
			if (x < 0)
			{
				continue;
			}

			if (aPattern[(x - start.x) % aPattern.size()])
			{
				DrawPixel({x, static_cast<int>(LERP(static_cast<float>(start.y),static_cast<float>(end.y), INVERSELERP(static_cast<float>(start.x),static_cast<float>(end.x),x)))}, aColor);
			}
		}
	}
	else
	{
		Point	start{aStart.x, MIN(aStart.y, aEnd.y)};
		Point	end{aEnd.x, MAX(aStart.y, aEnd.y)};
		for (int y = start.y; y <= end.y; y++)
		{
			if (y < 0)
			{
				continue;
			}

			if (aPattern[(y - start.y) % aPattern.size()])
			{
				DrawPixel({static_cast<int>(LERP(static_cast<float>(start.x), static_cast<float>(end.x), INVERSELERP(static_cast<float>(start.y), static_cast<float>(end.y), y))), y}, aColor);
			}
		}
	}

}

void
Canvas::DrawBezier(Point aStart, Point aC1, Point aC2, Point aEnd, V4F aColor, Pattern	aPattern)
{
	std::vector<Point> points;
	points.push_back(BezierInterpolate(aStart,aC1,aC2,aEnd,0.f));

	Point end = BezierInterpolate(aStart,aC1,aC2,aEnd,1.f);
	float at = 0.f;
	while (true)
	{
		float searchMax = 1.f;
		float searchMin = at;
		Point lastPoint = points.back();

		if (lastPoint.x == end.x && lastPoint.y == end.y)
		{
			break;
		}

		while (true)
		{
			float	midpoint	  = LERP(searchMin, searchMax, 0.5f);
			Point	nextPoint	  = BezierInterpolate(aStart, aC1, aC2, aEnd, midpoint);

			if (nextPoint.x == lastPoint.x && nextPoint.y == lastPoint.y)
			{
				searchMin = midpoint;
				continue;
			}

			if (abs(nextPoint.x - lastPoint.x) > 1 || abs(nextPoint.y - lastPoint.y) > 1)
			{
				searchMax = midpoint;
				continue;
			}

			points.push_back(nextPoint);
			at = midpoint;
			break;
		}
	}

	for (size_t i = 0; i < points.size(); i++)
	{
		if (aPattern[i%aPattern.size()])
		{
			DrawPixel(points[i],aColor);
		}
	}
}

float Bezier(std::vector<float> aPoints, float aValue)
{
	std::vector<float> points = aPoints;
	while (points.size() > 1)
	{
		for (size_t i = 0; i < points.size() - 1; i++)
		{
			points[i] = LERP(points[i],points[i+1],aValue);
		}
		points.pop_back();
	}
	return points[0];
}

Canvas::Point Canvas::BezierInterpolate(Point aStart, Point aC1, Point aC2, Point aEnd, float aValue)
{
	return 
	{
		static_cast<int>(Bezier({static_cast<float>(aStart.x) + 0.5f, static_cast<float>(aC1.x) + 0.5f, static_cast<float>(aC2.x) + 0.5f, static_cast<float>(aEnd.x) + 0.5f}, aValue)),
		static_cast<int>(Bezier({static_cast<float>(aStart.y) + 0.5f, static_cast<float>(aC1.y) + 0.5f, static_cast<float>(aC2.y) + 0.5f, static_cast<float>(aEnd.y) + 0.5f}, aValue)),
	};
}
