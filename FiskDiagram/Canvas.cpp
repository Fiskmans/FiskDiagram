Canvas::Canvas()
	: myHeight(0)
	, myWidth(0)
	, myTexture(nullptr)
	, myBlendMode(BlendMode::Add)
{
}

Canvas::~Canvas()
{
	delete[] myTexture;
}

void Canvas::Setup(size_t aWidth, size_t aHeight, V4F aClearColor)
{
	if (myTexture)
		delete[] myTexture;

	myWidth = aWidth;
	myHeight = aHeight;

	myTexture = new V4F[myWidth * myHeight];
	for (size_t i = 0; i < myWidth * myHeight; i++)
		myTexture[i] = V4F(aClearColor, 1);
}

ID3D11ShaderResourceView* Canvas::Export(ID3D11Device* aDevice)
{
	ID3D11ShaderResourceView* view;

	{
		D3D11_TEXTURE2D_DESC desc;
		WIPE(desc);
		desc.Width = myWidth;
		desc.Height = myHeight;
		desc.MipLevels = 1;
		desc.ArraySize = 1;
		desc.Format = DXGI_FORMAT_R32G32B32A32_FLOAT;
		desc.SampleDesc.Count = 1;
		desc.SampleDesc.Quality = 0;
		desc.Usage = D3D11_USAGE_DEFAULT;
		desc.BindFlags = D3D11_BIND_SHADER_RESOURCE;
		desc.CPUAccessFlags = 0;
		desc.MiscFlags = 0;

		D3D11_SUBRESOURCE_DATA data;
		WIPE(data);
		data.pSysMem = myTexture;
		data.SysMemPitch = myWidth * sizeof(V4F);
		data.SysMemSlicePitch = myWidth * myHeight * sizeof(V4F);


		ID3D11Texture2D* tex;
		HRESULT res = aDevice->CreateTexture2D(&desc, &data, &tex);

		if (FAILED(res))
		{
			LOGERROR("Failed to create texture");
			return nullptr;
		}

		D3D11_SHADER_RESOURCE_VIEW_DESC viewDesc;
		WIPE(viewDesc);

		viewDesc.Format = DXGI_FORMAT_R32G32B32A32_FLOAT;
		viewDesc.Texture2D.MipLevels = 1;
		viewDesc.Texture2D.MostDetailedMip = 0;
		viewDesc.ViewDimension = D3D_SRV_DIMENSION_TEXTURE2D;
		res = aDevice->CreateShaderResourceView(tex, &viewDesc, &view);

		tex->Release();
		if (FAILED(res))
		{
			LOGERROR("Failed to create view");
			return nullptr;
		}
	}

	return view;
}

size_t Canvas::GetWidth()
{
	return myWidth;
}

size_t Canvas::GetHeight()
{
	return myHeight;
}

void Canvas::DrawPixel(int aX, int aY, V4F aColor)
{
	if (aX < 0 || aY < 0 || aX >= myWidth || aY >= myHeight)
	{
		return;
	}
	size_t index = aY * myWidth + aX;
	switch (myBlendMode)
	{
	case BlendMode::Add:
		myTexture[index] = aColor + LERP(myTexture[index], V4F(0, 0, 0, 0), aColor.w);
		myTexture[index].x = CLAMP(0, 1, myTexture[index].x);
		myTexture[index].y = CLAMP(0, 1, myTexture[index].y);
		myTexture[index].z = CLAMP(0, 1, myTexture[index].z);
		break;
	case BlendMode::Replace:
		myTexture[index] = aColor;
		break;
	default:
		break;
	}
}

void Canvas::DrawBox(CommonUtilities::Vector2<int> aMin, CommonUtilities::Vector2<int> aMax, V4F aColor, bool aFilled)
{
	if (aFilled)
	{
		for (int y = aMin.y; y <= aMax.y; y++)
		{
			for (int x = aMin.x; x <= aMax.x; x++)
			{
				DrawPixel(x, y, aColor);
			}
		}
	}
}

void
Canvas::DrawText(const std::string& aText, CommonUtilities::Vector2<int> aBottomLeft, V4F aColor)
{
	CommonUtilities::Vector2<int> at = aBottomLeft;

	const static CommonUtilities::Vector2<size_t> glyphSize { 8, 13 };

	for (char c : aText)
	{
		for (int y = at.y; y < at.y + glyphSize.y; y++)
		{
			for (int x = at.x; x < at.x + 8; x++)
			{
				auto& glyph = glyphs[c-32];
				unsigned char bitMask = glyph[glyphSize.y + at.y - y - 1];
				if ((bitMask & BIT(glyphSize.x + at.x - x - 1)) != 0)
				{
					DrawPixel(x, y, aColor);
				}
			}
		}
		at.x += glyphSize.x + 1;
	}
}

void
Canvas::DrawLine(CommonUtilities::Vector2<int> aStart, CommonUtilities::Vector2<int> aEnd, V4F aColor, std::vector<bool> aPattern )
{
	CommonUtilities::Vector2<int>	start{MIN(aStart.x, aEnd.x), MIN(aStart.y, aEnd.y)};
	CommonUtilities::Vector2<int>	end{MAX(aStart.x, aEnd.x), MAX(aStart.y, aEnd.y)};

	if (end.x - start.x > end.y - start.y)
	{
		for (int x = start.x; x <= end.x; x++)
		{
			if (x < 0)
			{
				continue;
			}

			if (aPattern[(x - start.x) % aPattern.size()])
			{
				DrawPixel(x, LERP(static_cast<float>(start.y),static_cast<float>(end.y), INVERSELERP(static_cast<float>(start.x),static_cast<float>(end.x),x)), aColor);
			}
		}
	}
	else
	{
		for (int y = start.y; y <= end.y; y++)
		{
			if (y < 0)
			{
				continue;
			}

			if (aPattern[(y - start.y) % aPattern.size()])
			{
				DrawPixel(LERP(static_cast<float>(start.x), static_cast<float>(end.x), INVERSELERP(static_cast<float>(start.y), static_cast<float>(end.y), y)), y, aColor);
			}
		}
	}

}
